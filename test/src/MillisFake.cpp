#include <MillisFake.h>

/**************************************************************************************
 * GLOBAL VARIABLES
 **************************************************************************************/

static unsigned long millis_val = 0;

/**************************************************************************************
 * FUNCTION DEFINITION
 **************************************************************************************/

void set_millis(unsigned long const val)
{
  millis_val = val;
}

unsigned long millis()
{
  return millis_val;
}
